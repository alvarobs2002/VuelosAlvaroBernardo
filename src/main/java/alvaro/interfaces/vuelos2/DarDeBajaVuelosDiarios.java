    private void darDeBaja() {
        ArrayList<VueloDiario> lista = GestionVuelosDiarios.leerLista();
        ArrayList<VueloDiario> lista2 = new ArrayList<>();
        String codigo = txtCodigo.getText();
        boolean comp = comprobarExiste();
        if (comp) {
            for (VueloDiario v : lista) {
                if (v.getCodigoVuelo().equals(codigo)) {

                } else {
                    v.getCodigoVuelo();
                    v.getFechaVuelo();
                    v.getHoraSalida();
                    v.getHoraLlegada();
                    v.getNumeroPlazasOcupadas();
                    v.getPrecioVuelo();

                    lista2.add(v);
                }
            }
            String ruta = "src/main/java/archivos/vuelosDiarios.csv";
            reescribirCSV(lista2, ruta);
        }
    }

    private boolean comprobarExiste() {
        ArrayList<VueloDiario> lista = GestionVuelosDiarios.leerLista();
        String codigo = txtCodigo.getText();
        for (VueloDiario v : lista) {
            if (v.getCodigoVuelo().equals(codigo)) {
                lblInfo.setText("");
                return true;
            }
        }
        lblInfo.setText("El codigo no existe");
        return false;
    }

    public static void reescribirCSV(ArrayList<VueloDiario> listaObjetos, String rutaArchivo) {
        try (FileWriter writer = new FileWriter(rutaArchivo)) {
            for (VueloDiario objeto : listaObjetos) {
                // Adaptar los campos del objeto a una cadena CSV
                String csvLine = objeto.getCodigoVuelo() + "," + objeto.getFechaVuelo()+ "," + objeto.getHoraSalida() + ","
                        + objeto.getHoraLlegada() + "," + objeto.getNumeroPlazasOcupadas()+ "," + objeto.getPrecioVuelo()
                         + "\n";

                // Escribir la l√≠nea CSV en el archivo
                writer.write(csvLine);
            }

            System.out.println("Objetos guardados en formato CSV en " + rutaArchivo);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

