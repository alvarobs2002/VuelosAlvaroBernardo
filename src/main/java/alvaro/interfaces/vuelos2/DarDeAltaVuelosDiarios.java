    public static boolean comprobarCodigoVuelo(String codigo) {
        try (BufferedReader br = new BufferedReader(new FileReader("src/main/java/archivos/vuelos.csv"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] row = line.split(",");
                String codigoVuelo = row[0];
                if (codigo.equals(codigoVuelo)) {
                    return true;
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return false;
    }

