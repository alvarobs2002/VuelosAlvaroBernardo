    private void btnDarDeAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDarDeAltaActionPerformed
        // TODO add your handling code here:
        String codigoVuelo = txtCodigoVuelo.getText();
        String fechaVuelo = txtFechaVuelo.getText();
        String horaSalida = txtHoraSalida.getText();
        String horaLlegada = txtHoraLlegada.getText();
        String plazasOcupadas = txtNumeroPlazas.getText();
        String precio = txtPrecio.getText();
        if (comprobarCodigoVuelo(codigoVuelo)) {
            lblInfo.setText("");
            String dateHoraSalida = horaSalida;
            String dateHoraLlegada = horaLlegada;
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH:mm");
            DateTimeFormatter formatter2 = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            LocalDate localFechaVuelo = LocalDate.parse(fechaVuelo, formatter2);
            LocalTime localHoraSalida = LocalTime.parse(dateHoraSalida, formatter);
            LocalTime localHoraLlegada = LocalTime.parse(dateHoraLlegada, formatter);
            int intPlazasOcupadas = Integer.parseInt(plazasOcupadas);
            int intPrecio = Integer.parseInt(precio);

            VueloDiario vd = new VueloDiario(codigoVuelo, localFechaVuelo, localHoraSalida, localHoraLlegada, intPlazasOcupadas, intPrecio);
            String ruta = "src/main/java/archivos/vuelosDiarios.csv";
            guardarVuelosDiariosCSV(vd,ruta);
        } else {
            lblInfo.setText("El codigo del vuelo no existe");
        }
    }//GEN-LAST:event_btnDarDeAltaActionPerformed
    public static void guardarVuelosDiariosCSV(VueloDiario vd,String ruta) {
        try (FileWriter writer = new FileWriter(ruta, true)) {
            String csvLine = vd.getCodigoVuelo() + "," + vd.getFechaVuelo() + "," + vd.getHoraSalida() + "," + vd.getHoraLlegada() + "," + vd.getNumeroPlazasOcupadas()+ "," + vd.getPrecioVuelo()+ "\n";
            writer.write(csvLine);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
